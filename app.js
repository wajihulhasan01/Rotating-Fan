const a = document.querySelector("#img")
function On(){
    a.style.animationDuration = 1 + "s";
}
function Off(){
    a.style.animationDuration = 0 + "s";
}
function high(){
    a.style.animationDuration = 0.5 + "s";

}
function medium(){
    a.style.animationDuration = 1.5 + "s";

}
function low(){
    a.style.animationDuration = 2 + "s";

}